const __dirname = path.dirname(fileURLToPath(import.meta.url));
const rootDir = path.resolve(__dirname, "..");
const inputImageArg = process.argv[2] ? path.resolve(process.argv[2]) : null;
assert.ok(inputImageArg, "Pass an input image path, for example: npm run demo:input-to-layout-record -- D:\\download\\image.jpg");

const runStamp = new Date().toISOString().replaceAll(":", "-").replaceAll(".", "-");
const videoDir = path.join(rootDir, "outputs", "demo-videos", `input-to-html-layout-${runStamp}`);
const rawVideoDir = path.join(videoDir, "raw-video");
const viewport = { width: 1440, height: 980 };

function fileUrl(filePath) {
  return `file:///${path.resolve(filePath).replaceAll("\\", "/")}`;
}

function htmlEscape(value) {
  return String(value ?? "")
    .replaceAll("&", "&amp;")
    .replaceAll("<", "&lt;")
    .replaceAll(">", "&gt;")
    .replaceAll('"', "&quot;");
}

function listen(server) {
  return new Promise((resolve) => {
    server.listen(0, "127.0.0.1", () => resolve(server.address().port));
  });
}

function closeNodeServer(server) {
  return new Promise((resolve, reject) => {
    server.close((error) => (error ? reject(error) : resolve()));
  });
}

async function wait(ms) {
  await new Promise((resolve) => setTimeout(resolve, ms));
}

